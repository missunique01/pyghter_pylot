#Arithmetic Operators
a = 4
b = 2

#Addition
print(a+b)
#Subtraction
print(a-b)
#multiplication
print(a*b)
#Divison using single slash(/) returns the quotient as a float number
print(a/b)
#Division using 2 slashes(//) returns the quotient as a number no decimals
print(a//b)
#modulo division to get the Reminder
print(a%b)
# Square
print(a**b)


