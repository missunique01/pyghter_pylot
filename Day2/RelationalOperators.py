#Relational Operators
# the operators will give the Boolean value which True or False as output
# < > <= >= != ==
a = 10
b = 2

#Lessthan (<)
print(a<b)
#Greater than (>)
print(a>b)
#Lessthan or equal to (<=)
print(a<=b)
# Greater than or equal to (>=)
print(a>=b)
# Not equal to (!=)
print(a!=b)
# Equal to (==)
print(a==b)