# Logical Operators
# These are used along with Relational Operators
# LOgical Operators are AND  , OR, NOT
# In Logical AND returns only TRUE when all conditions are true, Returns false if any one condition is false
# In Logical OR returns False when all the conditions are False, Returns True if all the conditions
# are true & ANy one condition is true
# In Logical NOT, It will give opposite if it is TRUE it returns False vise versa

a = True
b = False

print(a and b) # returns False
print(a or b) # Returns True
print(not a) # Returns False