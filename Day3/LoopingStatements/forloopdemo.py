#for loop
# Print 1 to 10 numbers using for loop
for i in range(1,11):
    print(i)
print("Done")
# Print only even number between 1-20
for i in range(0,21,2):
    print(i)
print("Done")
#print odd numbers between 1 -20
for i in range(1,20,2):
    print(i)
print("Done")
#print 10 to 1 numbers in descending order
for i in range(10,0,-1):
    print(i)
print("Done")
# print 5,10,15,....100
for i in range(5,101,5):
    print(i)
print("Done")
